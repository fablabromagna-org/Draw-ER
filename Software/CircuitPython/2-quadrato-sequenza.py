import turtle

# Abbassa la penna sul foglio
turtle.pendown()


turtle.forward(150)  # va avanti di 100 passi
turtle.right(90)     # Gira a destra di 90 gradi

turtle.forward(150)
turtle.right(90)

turtle.forward(150)
turtle.right(90)

turtle.forward(150)
turtle.right(90)
turtle.done()

# spegne i motori e risparmia energia
turtle.done()
